              <p>KVM is a student founded and run 501c3.  It was seeded into the mind of Taylor Ottesen while on a research study abroad in India in 2012. Like many born and raised in the United States, the experience was enlightening and profoundly motivating.</p>
 
              <p>After returning home from four months in the southeastern port city of Visakhapatnam, Taylor couldn’t let go of his feelings for the people he had left behind; he needed to help share his knowledge and skills with them. Within a year, KVM was officially launched in the upstairs clubhouse room of his apartment building.</p>
               
              <p>Founded by university students who had a sincere desire to help but little practical experience, KVM has been a courageous experiment. With one of the founding students studying neuroscience and the other marketing, they had little knowledge of what organizing a foundation of this magnitude entailed. With lots of prayer and help from around the globe, KVM has grown in size and influence to become a force for good. Over the last ten months, nine additional students from three different universities and a few professionals scattered across the globe have joined us in this endeavor&mdash;and we hope you will as well.</p>
               
              <p>In June 2014 we opened our first school in Visakhapatnam with two classes of incredible students. We look forward to a second class starting at the beginning of 2015. We at KVM want to be the change that we want to see in the world. Welcome aboard.</p>

