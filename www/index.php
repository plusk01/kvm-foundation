                <p>In early 2013, KVM was born in the club house of an apartment complex in Provo, UT, by two undergraduate students of Brigham Young University and one freshman student at Utah Valley University. United simply by a desire “to anxiously engage in a good cause,” they hoped to make a mark on the world by educating the poor in third-world countries, starting with India. It seems random, right? Did they actually think they could impact lives in a country 8000 miles away? Why education? How is it that a couple of underqualified students would dream so big?</p>

                <p>The idea for KVM was planted in Taylor Ottesen’s mind while on a research study abroad in India two years ago. Like many born and raised in the first world, his first-hand experience with mass poverty was galvanizing and profoundly motivating.</p>

                <p>After returning home from four months in the south-eastern port city of Visakhapatnam, Taylor could not release the yearning he felt to do something to help the people he had left behind. For the next ten months, he fought a battle between the unyielding urge to do something ate away at him, and feelings of helplessness that almost overwhelmed him. Love overcame fear, and he opened up to his best friend Nic about his persistent feelings for India’s poor. They immediately discovered a mutual passion, Nic having believed for several years that any long-term impact on global poverty would have to come through education. Two weeks later, Taylor and Nic met with another college student looking to make a difference, and began talking about the questions listed above. The foundation that would become known as KVM had begun.</p>
