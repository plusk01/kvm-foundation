              <p>The major focuses of KVM are teaching skills and, eventually, job placement. We teach conversational English, self-reliance and confidence to young adults from financially-strained backgrounds. Many of our students have had years of English education and even have recognized degrees in their field&mdash;yet they lack the confidence and skills to communicate. Many of them can regurgitate information, sentences, and ideas they have been told, but cannot utilize the language to express themselves and their ideas.  We are piloting a replicable English model to turn rote English into English understanding and fluency. Our program teaches professional skills along with grammar and pronunciation and focuses on language confidence.</p>
               
              <p>After students complete the 4-month program, they will be have additional skills for job placement. We are in the process of contracting with several companies in Vishakhapatnam, India, to hire KVM graduates. As KVM becomes more recognized both in the city and globally, we anticipate that many companies will look to our company for their staffing needs, giving our students a stepping stone to accomplishing their own dreams.</p>


